#include<iostream>
using namespace std;
int main()
{
    int m,n,temp;
    cout<<"enter value of m ";
    cin>>m;
    cout<<"enter value of n";
    cin>>n;
    cout<<"before swaping"<<m<<endl;
    cout<<"before swaping"<<m<<endl;
    temp=m;
    m=n;
    n=temp;
    cout<<"after swaping"<<m<<endl;
    cout<<"after swaping"<<n<<endl;
    return 0;
}
